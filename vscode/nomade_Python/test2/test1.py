# days_of_week = ["Mon","Tue","Wed","Thu","Fri"]
name = "chonwoo"

# method는 데이터 뒤에 결합/연결된 function, name.결합메소드
print(name.upper())  # upper()메소드는 대문자로 변환
print(name.capitalize)  # 첫번째 글짜 대문자로 변환
print(name.startswith("n"))  # 시작숫자n맞아?  => True
print(name.replace("o","🥤")) # 새문자열로 바꿔줌
